Unary_Timer:	;; [timer]
		jsr 	XTUpdateClock
		lda 	ClockTicks
		sta 	esInt0,x
		lda 	ClockTicks+1
		sta 	esInt1,x
		lda 	ClockTicks+2
		sta 	esInt2,x
		lda 	#0
		sta 	esInt3,x
		sta 	esType,x
		rts


