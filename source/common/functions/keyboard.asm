; *****************************************************************************
;
;								Inkey command
;
; *****************************************************************************

Unary_Inkey:	;; [inkey]
		jsr 	XTGetKey
		sta 	esInt0,x
		jmp 	Int32Set8Bit

; *****************************************************************************
;
;								Get key command
;
; *****************************************************************************

Unary_Get:	;; [get]
		jsr 	Unary_Inkey
		lda 	esInt0,x
		beq 	Unary_Get
		rts		